// Read API key from .env
function getApiKey() {
  const envPath = path.join(__dirname, '..', '.env');
  const envContent = fs.readFileSync(envPath, 'utf-8');
  const match = envContent.match(/VITE_PEXELS_API_KEY=(.+)/);
  return match ? match[1].trim() : null;
}

