const FALLBACK_IMAGE = 'https://source.unsplash.com/800x600/?India,heritage';
const PEXELS_API = 'https://api.pexels.com/v1/search';

async function fetchImageForState(state) {
  try {
    const response = await fetch(
      `${PEXELS_API}?query=${encodeURIComponent(state.query)}&per_page=1`,
      {
        headers: { Authorization: API_KEY },
      }
    );

    if (!response.ok) {
      console.warn(`⚠️  Pexels API error for ${state.name}: ${response.status}`);
      return { ...state, image: FALLBACK_IMAGE, source: 'fallback' };
    }

    const data = await response.json();
    if (data.photos && data.photos.length > 0) {
      const imageUrl = data.photos[0].src.large;
      console.log(`✓ ${state.name}: ${imageUrl}`);
      return { ...state, image: imageUrl, source: 'pexels' };
    } else {
      console.warn(`⚠️  No images found for ${state.name}, using fallback`);
      return { ...state, image: FALLBACK_IMAGE, source: 'fallback' };
    }
  } catch (error) {
    console.error(`✗ Error fetching for ${state.name}:`, error.message);
    return { ...state, image: FALLBACK_IMAGE, source: 'fallback' };
  }
}

async function main() {
  console.log(`🔄 Fetching images for ${states.length} states/UTs...`);
  console.log('━'.repeat(60));

  const results = await Promise.all(states.map(fetchImageForState));

  // Build the mapImages object
  const mapImages = {};
  results.forEach(({ name, image }) => {
    mapImages[name] = image;
  });

  // Write to file
  const outputPath = path.join(__dirname, '..', 'src', 'data', 'mapImages.js');
  const fileContent = `// Auto-generated by scripts/fetchMapImages.js
// Do not edit manually
export const mapImages = ${JSON.stringify(mapImages, null, 2)};
`;

  fs.writeFileSync(outputPath, fileContent);

  console.log('━'.repeat(60));
  console.log(`✅ Done! Saved ${results.length} images to src/data/mapImages.js`);

  // Summary
  const pexelsCount = results.filter((r) => r.source === 'pexels').length;
  const fallbackCount = results.filter((r) => r.source === 'fallback').length;
  console.log(`   Pexels: ${pexelsCount}, Fallback: ${fallbackCount}`);
}

main().catch(console.error);
